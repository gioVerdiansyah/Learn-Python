# Kode C sebelumnya jika diubah ke dalam Python akan menjadi seperti berikut.
age = 17
salary = 50000.0

print(type(age))
print(type(salary))

# Output
# <class 'int'>
# <class 'float'>

# Dynamic Typing
x = 10
print(type(x))

x = "10"
print(type(x))

# <class 'int'>
# <class 'str'>

# Jadi kita dapat mengubah isi variabel seiring program berjalan